# Вихідні дані

T1 = 400  # Температура гарячої поверхні (K)
T2 = 300  # Температура холодної поверхні (K)
L = 0.05  # Товщина стінки (м)
k = 50  # Коефіцієнт теплопровідності матеріалу (Вт/м·К)

# Розрахунок теплового потоку

q = k * (T1 - T2) / L

# Виведення результату

print(f"Тепловий потік через стінку: {q:.2f} Вт/м²")
